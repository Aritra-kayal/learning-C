// relational operators
#include <stdio.h>
int main()
{
    // equal to
    printf("%d\n", 4 == 4);
    printf("%d\n", 4 == 3);
    // not equal to
    printf("%d\n", 4 != 4);
    printf("%d\n", 4 != 3);
    // greater than
    printf("%d\n", 4 > 3);
    printf("%d\n", 3 > 4);
    printf("%d\n", 4 > 4);
    // greater than or equal to
    printf("%d\n", 4 >= 4);
    printf("%d\n", 4 >= 3);
    // less than
    printf("%d\n", 4 < 3);
    printf("%d\n", 3 < 4);
    printf("%d\n", 4 < 4);
    // less than or equal to
    printf("%d\n", 4 <= 4);
    printf("%d\n", 4 <= 3);
    return 0;
}